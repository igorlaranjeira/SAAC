package Objetos;

public class Visto {
	private String visto;
	private int id;

	
	
	private Visto() {
		super();
	}
	
	public String getVisto() {
		return visto;
	}

	public void setVisto(String visto) {
		this.visto = visto;
	}
	private int getId() {
		return id;
	}
	private void setId(int id) {
		this.id = id;
	}
}
